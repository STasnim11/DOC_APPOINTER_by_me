-- Function 1: Get total appointment count for a doctor
CREATE OR REPLACE FUNCTION fn_get_doctor_appointment_count(
  p_doctor_id IN NUMBER
) RETURN NUMBER
IS
  v_count NUMBER;
BEGIN
  SELECT COUNT(*)
  INTO v_count
  FROM DOCTORS_APPOINTMENTS
  WHERE DOCTOR_ID = p_doctor_id;
  RETURN v_count;
END;
/

-- Function 2: Get total expenses for a patient (sum of doctor fees)
CREATE OR REPLACE FUNCTION fn_get_patient_total_expenses(
  p_patient_id IN NUMBER
) RETURN NUMBER
IS
  v_total NUMBER := 0;
BEGIN
  SELECT NVL(SUM(d.FEES), 0)
  INTO v_total
  FROM DOCTORS_APPOINTMENTS da
  INNER JOIN DOCTOR d ON da.DOCTOR_ID = d.ID
  WHERE da.PATIENT_ID = p_patient_id
    AND da.STATUS IN ('COMPLETED', 'BOOKED');
  RETURN v_total;
END;
/

-- Function 3: Calculate bed occupancy rate (by ward or overall)
CREATE OR REPLACE FUNCTION fn_calculate_bed_occupancy(
  p_ward_name IN VARCHAR2 DEFAULT NULL
) RETURN NUMBER
IS
  v_total_beds    NUMBER;
  v_occupied_beds NUMBER;
BEGIN
  IF p_ward_name IS NULL THEN
    SELECT COUNT(*) INTO v_total_beds FROM HOSPITAL_BEDS;
  ELSE
    SELECT COUNT(*) INTO v_total_beds
    FROM HOSPITAL_BEDS WHERE WARD_NAME = p_ward_name;
  END IF;

  IF v_total_beds = 0 THEN RETURN 0; END IF;

  IF p_ward_name IS NULL THEN
    SELECT COUNT(*) INTO v_occupied_beds
    FROM HOSPITAL_BEDS WHERE UPPER(STATUS) = 'OCCUPIED';
  ELSE
    SELECT COUNT(*) INTO v_occupied_beds
    FROM HOSPITAL_BEDS
    WHERE WARD_NAME = p_ward_name AND UPPER(STATUS) = 'OCCUPIED';
  END IF;

  RETURN ROUND((v_occupied_beds * 100.0) / v_total_beds, 2);
END;
/

