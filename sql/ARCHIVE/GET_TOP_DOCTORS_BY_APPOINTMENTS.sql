-- SQL Query to get top doctors with highest number of appointments
-- This query counts total appointments per doctor and orders by count

SELECT 
    d.ID as DOCTOR_ID,
    u.NAME as DOCTOR_NAME,
    u.EMAIL as DOCTOR_EMAIL,
    d.DEGREES,
    d.EXPERIENCE_YEARS,
    d.FEES,
    s.NAME as SPECIALTY,
    COUNT(da.ID) as TOTAL_APPOINTMENTS
FROM DOCTOR d
JOIN USERS u ON d.USER_ID = u.ID
LEFT JOIN DOC_SPECIALIZATION ds ON d.ID = ds.DOCTOR_ID
LEFT JOIN SPECIALIZATION s ON ds.SPECIALIZATION_ID = s.ID
LEFT JOIN DOCTORS_APPOINTMENTS da ON d.ID = da.DOCTOR_ID
WHERE u.ROLE = 'DOCTOR'
GROUP BY d.ID, u.NAME, u.EMAIL, d.DEGREES, d.EXPERIENCE_YEARS, d.FEES, s.NAME
ORDER BY TOTAL_APPOINTMENTS DESC, u.NAME ASC
FETCH FIRST 10 ROWS ONLY;

-- Alternative: Get only doctors with at least 1 appointment
SELECT 
    d.ID as DOCTOR_ID,
    u.NAME as DOCTOR_NAME,
    u.EMAIL as DOCTOR_EMAIL,
    d.DEGREES,
    d.EXPERIENCE_YEARS,
    d.FEES,
    s.NAME as SPECIALTY,
    COUNT(da.ID) as TOTAL_APPOINTMENTS
FROM DOCTOR d
JOIN USERS u ON d.USER_ID = u.ID
LEFT JOIN DOC_SPECIALIZATION ds ON d.ID = ds.DOCTOR_ID
LEFT JOIN SPECIALIZATION s ON ds.SPECIALIZATION_ID = s.ID
INNER JOIN DOCTORS_APPOINTMENTS da ON d.ID = da.DOCTOR_ID
WHERE u.ROLE = 'DOCTOR'
GROUP BY d.ID, u.NAME, u.EMAIL, d.DEGREES, d.EXPERIENCE_YEARS, d.FEES, s.NAME
HAVING COUNT(da.ID) > 0
ORDER BY TOTAL_APPOINTMENTS DESC, u.NAME ASC
FETCH FIRST 10 ROWS ONLY;
