-- ============================================
-- CREATE BILLS TABLE
-- Run this BEFORE creating the sp_generate_bill procedure
-- ============================================

CREATE TABLE BILLS (
    ID NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    APPOINTMENT_ID NUMBER NOT NULL,
    PATIENT_ID NUMBER NOT NULL,
    DOCTOR_ID NUMBER NOT NULL,
    CONSULTATION_FEE NUMBER DEFAULT 0,
    MEDICINE_COST NUMBER DEFAULT 0,
    TEST_COST NUMBER DEFAULT 0,
    TOTAL_AMOUNT NUMBER NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'PENDING',
    ISSUE_DATE DATE DEFAULT SYSDATE,
    PAID_DATE DATE,
    PAYMENT_METHOD VARCHAR2(50),
    NOTES VARCHAR2(500),
    CONSTRAINT fk_bill_appointment FOREIGN KEY (APPOINTMENT_ID) 
        REFERENCES DOCTORS_APPOINTMENTS(ID) ON DELETE CASCADE,
    CONSTRAINT fk_bill_patient FOREIGN KEY (PATIENT_ID) 
        REFERENCES PATIENT(ID) ON DELETE CASCADE,
    CONSTRAINT fk_bill_doctor FOREIGN KEY (DOCTOR_ID) 
        REFERENCES DOCTOR(ID) ON DELETE CASCADE,
    CONSTRAINT chk_bill_status CHECK (STATUS IN ('PENDING', 'PAID', 'CANCELLED'))
);

-- Create index for faster queries
CREATE INDEX idx_bills_appointment ON BILLS(APPOINTMENT_ID);
CREATE INDEX idx_bills_patient ON BILLS(PATIENT_ID);
CREATE INDEX idx_bills_status ON BILLS(STATUS);

-- Verify table was created
DESC BILLS;

SELECT '✅ BILLS table created successfully!' as MESSAGE FROM DUAL;
